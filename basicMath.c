#include "myMath.h"

// returns x + y
float add(float x , float y){
    float sum;
    sum = x+y;
    return sum;
} 

// returns x - y
float sub(float x , float y){
    float sum;
    sum = x-y;
    return sum;
} 

// returns y*x
double mul(double x , int y){
    double sum;
    sum = x*y;
    return sum;
}

// returns x/y
double div(double x, int y){
        double sum;
        sum = x/y;
        return sum;   
    }

    
 